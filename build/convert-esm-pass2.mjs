#!/usr/bin/env node
/**
 * Ellis Web Bell 6.0.0 converter — PASS 2: imports/exports + entry point
 *
 * For every chunk in src/js/:
 *  - computes the names it references that are declared in other chunks
 *    (via eslint-scope; after pass 1 there are no cross-module writes left)
 *  - prepends a generated import block
 *  - appends a generated export block (the names other chunks import from it)
 *  - writes src/js/main.js importing every chunk in manifest order
 *
 * Aborts on: any remaining cross-module write, any unresolved name that is
 * neither a chunk symbol nor a browser/JS global.
 */
import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import * as espree from 'espree';
import * as eslintScope from 'eslint-scope';
import globalsPkg from 'globals';

const here = dirname(fileURLToPath(import.meta.url));
const srcDir = join(here, '..', 'src', 'js');

const KNOWN_GLOBALS = new Set([
  ...Object.keys(globalsPkg.browser),
  ...Object.keys(globalsPkg.builtin ?? {}),
  'Tone', 'globalThis', 'undefined',
]);

const files = readdirSync(srcDir).filter(f => f.endsWith('.js') && f !== 'main.js').sort();

const parsed = new Map();
for (const f of files) {
  const code = readFileSync(join(srcDir, f), 'utf8');
  const ast = espree.parse(code, { ecmaVersion: 'latest', sourceType: 'module', range: true, loc: true });
  const sm = eslintScope.analyze(ast, { ecmaVersion: 2023, sourceType: 'module' });
  parsed.set(f, { code, sm, moduleScope: sm.globalScope.childScopes[0] });
}

const symbolTable = new Map();
for (const [f, p] of parsed) for (const v of p.moduleScope.variables) {
  if (symbolTable.has(v.name)) throw new Error(`duplicate symbol ${v.name} in ${symbolTable.get(v.name)} and ${f}`);
  symbolTable.set(v.name, f);
}

const importsOf = new Map(); // file -> Map(owner -> Set(names))
const exportsOf = new Map(files.map(f => [f, new Set()]));
const problems = [];

for (const [f, p] of parsed) {
  const imp = new Map();
  for (const ref of p.sm.globalScope.through) {
    const name = ref.identifier.name;
    const owner = symbolTable.get(name);
    if (owner && owner !== f) {
      if (ref.isWrite()) problems.push(`WRITE to foreign ${name} at ${f}:${ref.identifier.loc.start.line}`);
      if (!imp.has(owner)) imp.set(owner, new Set());
      imp.get(owner).add(name);
      exportsOf.get(owner).add(name);
    } else if (!owner && !KNOWN_GLOBALS.has(name)) {
      problems.push(`UNRESOLVED ${name} at ${f}:${ref.identifier.loc.start.line}`);
    }
  }
  importsOf.set(f, imp);
}
if (problems.length) {
  console.error(problems.join('\n'));
  throw new Error(`${problems.length} problems — aborting, nothing written`);
}

function formatImport(names, from) {
  const sorted = [...names].sort();
  const oneLine = `import { ${sorted.join(', ')} } from './${from}';`;
  if (oneLine.length <= 100) return oneLine;
  let lines = ['import {'];
  let cur = '   ';
  for (const n of sorted) {
    if ((cur + ' ' + n + ',').length > 96) { lines.push(cur); cur = '   '; }
    cur += ' ' + n + ',';
  }
  lines.push(cur);
  lines.push(`} from './${from}';`);
  return lines.join('\n');
}

for (const f of files) {
  let code = parsed.get(f).code;
  const imp = importsOf.get(f);
  const exp = [...exportsOf.get(f)].sort();

  let header = '';
  if (imp.size > 0) {
    header += '// ===== module imports (6.0.0; generated by build/convert-esm-pass2.mjs) =====\n';
    for (const owner of [...imp.keys()].sort()) {
      header += formatImport(imp.get(owner), owner) + '\n';
    }
    header += '\n';
  }
  let footer = '';
  if (exp.length > 0) {
    footer = `\n// ===== module exports (6.0.0) =====\nexport {\n`;
    for (const n of exp) footer += `    ${n},\n`;
    footer += `};\n`;
  }
  writeFileSync(join(srcDir, f), header + code + footer);
}

// entry point: preserve original concatenation order for side-effect timing
const manifestOrder = files.filter(f => f !== 'state.js');
let main = `// Ellis Web Bell 6.0.0 — module entry point.
// index.html loads THIS file (<script type="module" src="src/js/main.js">).
// Import order preserves the pre-7.0 concatenation order of script.js;
// modules also import each other directly, so evaluation order is the
// dependency-correct refinement of this list. script.js no longer exists.
import './state.js';
`;
for (const f of manifestOrder) main += `import './${f}';\n`;
writeFileSync(join(srcDir, 'main.js'), main);

console.log(`pass 2 complete: ${files.length} chunks + main.js`);
for (const f of files) {
  const nImp = [...importsOf.get(f).values()].reduce((s, x) => s + x.size, 0);
  console.log(`  ${f}: imports ${nImp}, exports ${exportsOf.get(f).size}`);
}
